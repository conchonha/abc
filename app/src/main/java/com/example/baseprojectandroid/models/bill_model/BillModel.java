package com.example.baseprojectandroid.models.bill_model;

public class BillModel {
    String mImageBill;
    String mBillCode;
    String mDate;
    String mSumBill;

    public String getmImageBill() {
        return mImageBill;
    }

    public void setmImageBill(String mImageBill) {
        this.mImageBill = mImageBill;
    }

    public String getmBillCode() {
        return mBillCode;
    }

    public void setmBillCode(String mBillCode) {
        this.mBillCode = mBillCode;
    }

    public String getmDate() {
        return mDate;
    }

    public void setmDate(String mDate) {
        this.mDate = mDate;
    }

    public String getmSumBill() {
        return mSumBill;
    }

    public void setmSumBill(String mSumBill) {
        this.mSumBill = mSumBill;
    }
}
